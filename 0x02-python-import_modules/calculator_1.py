#!/usr/bin/python3
def add(a, b):
    """My addition function args & returns

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My subtraction function args & returns

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplication function args & returns

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """My division function args & returns

    Args:
        a: first integer
        b: second integer

    Returns:
	 The return value. a / b
    """
    return int(a / b)
